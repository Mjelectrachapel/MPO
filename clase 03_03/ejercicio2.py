numero1= float(input("Introduce un número\n"))
numero2= float(input("Introduce otro número\n"))
print(f"La suma de esos dos números es: {numero1+numero2:.2f}") #:.2f lo que hace es poner sólo 2 decimales
print(f"La resta de esos dos números es: {numero1-numero2:.2f}")
print(f"La multiplicación de esos dos números es: {numero1*numero2:.2f}")
print(f"El resultado de la división de esos dos números es: {numero1/numero2:.2f}")
